CREATE TABLE IF NOT EXISTS PUBLIC.USERS (
	USER_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	EMAIL VARCHAR(255),
    LOGIN VARCHAR(100) NOT NULL,
    NAME VARCHAR(255),
	BIRTHDAY DATE NOT NULL,
	CONSTRAINT USERS_PK PRIMARY KEY (USER_ID)
);

CREATE TABLE IF NOT EXISTS PUBLIC.FRIENDS (
	FRIEND_ID INTEGER NOT NULL,
	USER_ID INTEGER NOT NULL,
	CONSTRAINT FRIENDS_PK PRIMARY KEY (USER_ID, FRIEND_ID),
    CONSTRAINT FRIENDS_USER_FK FOREIGN KEY (USER_ID) 
        REFERENCES PUBLIC.USERS(USER_ID) ON DELETE CASCADE,
    CONSTRAINT FRIENDS_FRIEND_FK FOREIGN KEY (FRIEND_ID) 
        REFERENCES PUBLIC.USERS(USER_ID) ON DELETE CASCADE,
    CONSTRAINT FRIENDS_NO_SELF_FRIEND CHECK (USER_ID != FRIEND_ID)
);

CREATE TABLE IF NOT EXISTS PUBLIC.MPA_RATINGS (
	MPA_RATING_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	NAME VARCHAR(50) NOT NULL,
	CONSTRAINT MPA_RATINGS_PK PRIMARY KEY (MPA_RATING_ID)
);

CREATE TABLE IF NOT EXISTS PUBLIC.FILMS (
	FILM_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	NAME VARCHAR(255) NOT NULL,
	DESCRIPTION VARCHAR(200),
	RELEASEDATE DATE,
	DURATION INTEGER CHECK (DURATION > 0),
	MPA_RATING_ID INTEGER,
	CONSTRAINT FILMS_PK PRIMARY KEY (FILM_ID),
	CONSTRAINT FILMS_MPA_RATINGS_FK FOREIGN KEY (MPA_RATING_ID) REFERENCES PUBLIC.MPA_RATINGS(MPA_RATING_ID)
);

CREATE TABLE IF NOT EXISTS PUBLIC.GENRES (
	GENRE_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	NAME VARCHAR(255) NOT NULL UNIQUE,
	CONSTRAINT GENRES_PK PRIMARY KEY (GENRE_ID)
);

CREATE TABLE IF NOT EXISTS PUBLIC.FILMS_GENRES (
	GENRE_ID INTEGER NOT NULL,
	FILM_ID INTEGER NOT NULL,
	CONSTRAINT FILMS_GENRES_PK PRIMARY KEY (GENRE_ID, FILM_ID),
	CONSTRAINT FILMS_GENRES_G_FK FOREIGN KEY (GENRE_ID) 
        REFERENCES PUBLIC.GENRES(GENRE_ID) ON DELETE CASCADE,
    CONSTRAINT FILMS_GENRES_F_FK FOREIGN KEY (FILM_ID) 
        REFERENCES PUBLIC.FILMS(FILM_ID) ON DELETE CASCADE
);


CREATE TABLE IF NOT EXISTS PUBLIC.FILM_LIKES (
	FILM_ID INTEGER NOT NULL,
	USER_ID INTEGER NOT NULL,
	CONSTRAINT FILM_LIKES_PK PRIMARY KEY (FILM_ID, USER_ID),
	CONSTRAINT FILM_LIKES_F_FK FOREIGN KEY (FILM_ID) 
        REFERENCES PUBLIC.FILMS(FILM_ID) ON DELETE CASCADE,
    CONSTRAINT FILM_LIKES_U_FK FOREIGN KEY (USER_ID) 
        REFERENCES PUBLIC.USERS(USER_ID) ON DELETE CASCADE
);


